"""
//////////////////////////////////////////////////////////////////////
//  Method name : fetch_data()
//  input       : url
//  output      : html page text
//////////////////////////////////////////////////////////////////////
"""

def fetch_data(url, headers = None):

    r = requests.get(url, headers = headers)
    data = r.text
    return data


